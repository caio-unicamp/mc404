int middle_value_int(int *array, int n){
    int middle = n / 2;
    return array[middle];
};

short middle_value_short(short *array, int n){
    int middle = n / 2;
    return array[middle];
};

char middle_value_char(char *array, int n){
    int middle = n / 2;
    return array[middle];
};

int value_matrix(int matrix[12][42], int r, int c){
    return matrix[r][c];
};
